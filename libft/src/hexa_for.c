#include "libft.h"
#include <unistd.h>

void		hexa_for(int nb)
{
	size_t		y;
	char		*base;

	base = "0123456789ABCDEF";
	if (nb >= 16)
		hexa_for(nb / 16);
	y = nb % 16;
	write(1, &base[y], 1);
}
